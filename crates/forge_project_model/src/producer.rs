fn run_command_with_timeout(
    executable: &Path,
    args: &[&str],
    timeout: Duration,
) -> RustAnalyzerProcessOutput {
    let mut child = match Command::new(executable)
        .args(args)
        .stdin(Stdio::null())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
    {
        Ok(child) => child,
        Err(_) => {
            return RustAnalyzerProcessOutput {
                exit_code: None,
                stdout: Vec::new(),
                stderr: Vec::new(),
                timed_out: false,
            };
        }
    };
    let deadline = Instant::now()
        .checked_add(timeout)
        .unwrap_or_else(Instant::now);
    loop {
        match child.try_wait() {
            Ok(Some(_status)) => match child.wait_with_output() {
                Ok(output) => {
                    return RustAnalyzerProcessOutput {
                        exit_code: output.status.code(),
                        stdout: output.stdout,
                        stderr: output.stderr,
                        timed_out: false,
                    };
                }
                Err(_) => {
                    return RustAnalyzerProcessOutput {
                        exit_code: None,
                        stdout: Vec::new(),
                        stderr: Vec::new(),
                        timed_out: false,
                    };
                }
            },
            Ok(None) if Instant::now() >= deadline => {
                let _ = child.kill();
                let _ = child.wait();
                return RustAnalyzerProcessOutput {
                    exit_code: None,
                    stdout: Vec::new(),
                    stderr: Vec::new(),
                    timed_out: true,
                };
            }
            Ok(None) => std::thread::sleep(Duration::from_millis(10)),
            Err(_) => {
                return RustAnalyzerProcessOutput {
                    exit_code: None,
                    stdout: Vec::new(),
                    stderr: Vec::new(),
                    timed_out: false,
                };
            }
        }
    }
}

