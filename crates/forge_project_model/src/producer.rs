/// Explicit loss marker for bounded native LSP request derivation.
#[derive(Clone, Debug, Default, PartialEq, Eq)]
pub struct NativeLspBoundedLoss {
    /// Eligible endpoint positions omitted because `max_endpoints` was reached.
    pub omitted_endpoint_positions: usize,
    /// Eligible files omitted from `didOpen` because `max_files` was reached.
    pub omitted_open_files: usize,
}

impl NativeLspBoundedLoss {
    /// Creates an explicit bounded-loss marker.
    ///
    /// # Arguments
    ///
    /// * `omitted_endpoint_positions` - Number of eligible endpoint positions omitted by bounds.
    /// * `omitted_open_files` - Number of eligible open-plan files omitted by bounds.
    pub fn new(omitted_endpoint_positions: usize, omitted_open_files: usize) -> Self {
        Self { omitted_endpoint_positions, omitted_open_files }
    }

    /// Returns an explicit no-loss marker.
    pub fn none() -> Self {
        Self::default()
    }

    /// Returns true when derivation intentionally produced a bounded partial view.
    pub fn is_lossy(&self) -> bool {
        self.omitted_endpoint_positions > 0 || self.omitted_open_files > 0
    }
}

/// Explicit single-request native rust-analyzer LSP request.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct NativeLspReferenceRequest {
    /// Base external fact production metadata.
    pub production: ExternalFactProductionRequest,
    /// One manifest-owned typed source endpoint.
    pub source: NativeLspSourceEndpoint,
    /// Explicit manifest-owned target endpoint position mapping.
    pub endpoint_positions: Vec<NativeLspEndpointPosition>,
    /// Bounded manifest-owned didOpen plan.
    pub open_plan: Vec<NativeLspFileOpenPlan>,
    /// Expected JSON-RPC response identifier for textDocument/references.
    pub response_id: u64,
    /// Transport, parser, and process bounds.
    pub bounds: RustAnalyzerBounds,
    /// Explicit marker for endpoints/files omitted by bounded derivation.
    pub bounded_loss: NativeLspBoundedLoss,
}

impl NativeLspReferenceRequest {
    /// Creates an explicit single-request native LSP reference request.
    ///
    /// # Arguments
    ///
    /// * `production` - External fact metadata and reference fact bound.
    /// * `source` - One typed manifest-owned source endpoint and position.
    /// * `endpoint_positions` - Explicit response-location endpoint mapping.
    /// * `open_plan` - Bounded didOpen plan for manifest-owned source files.
    /// * `response_id` - Expected `textDocument/references` response id.
    /// * `bounds` - Transport, parser, and process limits.
    pub fn new(
        production: ExternalFactProductionRequest,
        source: NativeLspSourceEndpoint,
        endpoint_positions: Vec<NativeLspEndpointPosition>,
        open_plan: Vec<NativeLspFileOpenPlan>,
        response_id: u64,
        bounds: RustAnalyzerBounds,
    ) -> Self {
        Self {
            production,
            source,
            endpoint_positions,
            open_plan,
            response_id,
            bounds,
            bounded_loss: NativeLspBoundedLoss::none(),
        }
    }
    /// Attaches explicit bounded-loss semantics to this request.
    ///
    /// # Arguments
    ///
    /// * `bounded_loss` - Endpoint/file omission counts caused by request bounds.
    pub fn with_bounded_loss(mut self, bounded_loss: NativeLspBoundedLoss) -> Self {
        self.bounded_loss = bounded_loss;
        self
    }
}

/// Typed result of deterministic native LSP reference request derivation.
#[allow(clippy::large_enum_variant)]
#[derive(Clone, Debug, PartialEq, Eq)]
pub enum NativeLspReferenceRequestDerivation {
    /// Exactly one manifest-owned request was derived.
    Request(NativeLspReferenceRequest),
    /// No eligible endpoint could be proven without guessing.
    NoEligibleEndpoint(NativeLspNoEligibleEndpoint),
}

/// Typed no-op reason for native LSP reference request derivation.
#[derive(Clone, Debug, PartialEq, Eq)]
pub enum NativeLspNoEligibleEndpoint {
    /// Manifest has no Rust symbol whose identifier token can be proven in
    /// manifest-owned source text.
    NoRustSymbolWithExactIdentifierToken,
    /// Source text was missing for all candidate Rust symbol files.
    MissingManifestOwnedSourceText,
    /// Source text content did not match manifest file hashes.
    SourceTextHashMismatch,
    /// The request or endpoint mapping would exceed configured bounds.
    BoundsExceeded,
}

/// Deterministically derives at most one native LSP `textDocument/references` request.
///
/// The helper reads only caller-supplied manifest-owned source text. It selects
/// the first eligible Rust symbol in deterministic `(path, start_line, id)` order,
/// proves the symbol identifier token occurs on the manifest symbol start line,
/// computes the zero-based UTF-16 LSP character offset, and builds an explicit
/// bounded response-location endpoint/open-file plan. When the workspace has
/// more eligible endpoint positions or open files than the configured bounds,
/// the request still remains safe and deterministic, but carries
/// [`NativeLspBoundedLoss`] counts so callers can treat the artifact as a
/// bounded partial exact-fact slice rather than complete workspace coverage. It
/// returns a typed no-op when the source position cannot be proven exactly.
///
/// # Arguments
///
/// * `manifest` - Frozen project manifest that owns files and symbols.
/// * `source_texts` - Complete source text keyed by manifest-relative path.
/// * `production` - External fact production metadata and fact bound.
/// * `bounds` - Native LSP transport and normalization bounds.
pub fn derive_native_lsp_reference_request(
    manifest: &ProjectManifest,
    source_texts: &BTreeMap<String, String>,
    production: ExternalFactProductionRequest,
    bounds: RustAnalyzerBounds,
) -> NativeLspReferenceRequestDerivation {
    let mut hash_mismatch_observed = false;
    let mut missing_text_observed = false;
    let mut candidates = manifest
        .symbols
        .iter()
        .filter(|symbol| is_native_lsp_reference_symbol_kind(&symbol.kind))
        .collect::<Vec<_>>();
    candidates.sort_by(|left, right| {
        left.path
            .cmp(&right.path)
            .then_with(|| left.start_line.cmp(&right.start_line))
            .then_with(|| left.id.cmp(&right.id))
    });
    for symbol in candidates {
        let Some(file) = manifest.files.iter().find(|file| file.path == symbol.path) else {
            continue;
        };
        if file.language != Language::Rust {
            continue;
        }
        let Some(source_text) = source_texts.get(&symbol.path) else {
            missing_text_observed = true;
            continue;
        };
        if fingerprint(source_text) != file.content_hash {
            hash_mismatch_observed = true;
            continue;
        }
        let Some((line, character)) = exact_symbol_identifier_position(source_text, symbol) else {
            continue;
        };
        let Some(endpoint_plan) = derive_native_endpoint_positions(
            manifest,
            source_texts,
            &symbol.path,
            bounds.max_endpoints,
        ) else {
            continue;
        };
        let Some(open_plan) = derive_native_open_plan(
            manifest,
            source_texts,
            &symbol.path,
            &endpoint_plan.positions,
            bounds.max_files,
        ) else {
            continue;
        };
        if production.max_reference_facts > bounds.max_references
            || bounds.max_files == 0
            || native_lsp_outbound_message_count(open_plan.files.len()) > bounds.max_messages
        {
            return NativeLspReferenceRequestDerivation::NoEligibleEndpoint(
                NativeLspNoEligibleEndpoint::BoundsExceeded,
            );
        }
        let request = NativeLspReferenceRequest::new(
            production,
            NativeLspSourceEndpoint::new(symbol.path.clone(), symbol.id.clone(), line, character),
            endpoint_plan.positions,
            open_plan.files,
            7,
            bounds,
        )
        .with_bounded_loss(NativeLspBoundedLoss::new(
            endpoint_plan.omitted_positions,
            open_plan.omitted_files,
        ));
        return NativeLspReferenceRequestDerivation::Request(request);
    }
    if hash_mismatch_observed {
        NativeLspReferenceRequestDerivation::NoEligibleEndpoint(
            NativeLspNoEligibleEndpoint::SourceTextHashMismatch,
        )
    } else if missing_text_observed {
        NativeLspReferenceRequestDerivation::NoEligibleEndpoint(
            NativeLspNoEligibleEndpoint::MissingManifestOwnedSourceText,
        )
    } else {
        NativeLspReferenceRequestDerivation::NoEligibleEndpoint(
            NativeLspNoEligibleEndpoint::NoRustSymbolWithExactIdentifierToken,
        )
    }
}

fn is_native_lsp_reference_symbol_kind(kind: &SymbolKind) -> bool {
    matches!(
        kind,
        SymbolKind::Struct
            | SymbolKind::Enum
            | SymbolKind::Trait
            | SymbolKind::Function
            | SymbolKind::Method
            | SymbolKind::Test
            | SymbolKind::Module
    )
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct NativeOpenPlanDerivation {
    files: Vec<NativeLspFileOpenPlan>,
    omitted_files: usize,
}

#[derive(Clone, Debug, PartialEq, Eq)]
struct NativeEndpointPositionDerivation {
    positions: Vec<NativeLspEndpointPosition>,
    omitted_positions: usize,
}

fn derive_native_open_plan(
    manifest: &ProjectManifest,
    source_texts: &BTreeMap<String, String>,
    source_path: &str,
    endpoint_positions: &[NativeLspEndpointPosition],
    max_files: usize,
) -> Option<NativeOpenPlanDerivation> {
    if max_files == 0 {
        return None;
    }
    let mut candidate_paths = Vec::new();
    candidate_paths.push(source_path.to_string());
    for position in endpoint_positions {
        if !candidate_paths.iter().any(|path| path == &position.path) {
            candidate_paths.push(position.path.clone());
        }
    }
    let omitted_files = candidate_paths.len().saturating_sub(max_files);
    let mut open_plan = Vec::new();
    for path in candidate_paths.into_iter().take(max_files) {
        let file = manifest.files.iter().find(|file| file.path == path)?;
        if file.language != Language::Rust {
            continue;
        }
        let source_text = source_texts.get(&file.path)?;
        if fingerprint(source_text) != file.content_hash {
            return None;
        }
        open_plan.push(NativeLspFileOpenPlan::new(
            file.path.clone(),
            source_text.clone(),
        ));
    }
    if open_plan.iter().any(|file| file.path == source_path) {
        Some(NativeOpenPlanDerivation { files: open_plan, omitted_files })
    } else {
        None
    }
}

fn derive_native_endpoint_positions(
    manifest: &ProjectManifest,
    source_texts: &BTreeMap<String, String>,
    source_path: &str,
    max_endpoints: usize,
) -> Option<NativeEndpointPositionDerivation> {
    if max_endpoints == 0 {
        return None;
    }
    let mut positions = Vec::new();
    let mut symbols = manifest
        .symbols
        .iter()
        .filter(|symbol| is_native_lsp_reference_symbol_kind(&symbol.kind))
        .collect::<Vec<_>>();
    symbols.sort_by(|left, right| {
        (left.path != source_path)
            .cmp(&(right.path != source_path))
            .then_with(|| left.path.cmp(&right.path))
            .then_with(|| left.start_line.cmp(&right.start_line))
            .then_with(|| left.id.cmp(&right.id))
    });
    for symbol in symbols {
        let file = manifest
            .files
            .iter()
            .find(|file| file.path == symbol.path)?;
        if file.language != Language::Rust {
            continue;
        }
        let source_text = source_texts.get(&symbol.path)?;
        if fingerprint(source_text) != file.content_hash {
            return None;
        }
        let (line, character) = exact_symbol_identifier_position(source_text, symbol)?;
        positions.push(NativeLspEndpointPosition::new(
            symbol.path.clone(),
            line,
            character,
            symbol.id.clone(),
        ));
    }
    let mut files = manifest
        .files
        .iter()
        .filter(|file| file.language == Language::Rust)
        .collect::<Vec<_>>();
    files.sort_by(|left, right| {
        (left.path != source_path)
            .cmp(&(right.path != source_path))
            .then_with(|| left.path.cmp(&right.path))
    });
    for file in files {
        let source_text = source_texts.get(&file.path)?;
        if fingerprint(source_text) != file.content_hash {
            return None;
        }
        positions.push(NativeLspEndpointPosition::new(
            file.path.clone(),
            0,
            0,
            file.path.clone(),
        ));
    }
    let omitted_positions = positions.len().saturating_sub(max_endpoints);
    positions.truncate(max_endpoints);
    Some(NativeEndpointPositionDerivation { positions, omitted_positions })
}

fn exact_symbol_identifier_position(
    source_text: &str,
    symbol: &crate::types::SymbolNode,
) -> Option<(u32, u32)> {
    let zero_based_line = symbol.start_line.checked_sub(1)?;
    let line_text = source_text
        .lines()
        .nth(usize::try_from(zero_based_line).ok()?)?;
    let byte_start = exact_identifier_token_byte_start(line_text, &symbol.name)?;
    let character = utf16_character_offset(line_text, byte_start)?;
    Some((zero_based_line, character))
}

fn exact_identifier_token_byte_start(line_text: &str, identifier: &str) -> Option<usize> {
    if identifier.is_empty() || !is_rust_identifier(identifier) {
        return None;
    }
    let mut matches = line_text
        .match_indices(identifier)
        .filter_map(|(start, _)| {
            let end = start.checked_add(identifier.len())?;
            if is_identifier_boundary(line_text, start, end) {
                Some(start)
            } else {
                None
            }
        });
    let first = matches.next()?;
    if matches.next().is_some() {
        None
    } else {
        Some(first)
    }
}

fn is_identifier_boundary(line_text: &str, start: usize, end: usize) -> bool {
    let Some(before_text) = line_text.get(..start) else {
        return false;
    };
    let Some(after_text) = line_text.get(end..) else {
        return false;
    };
    let before = before_text
        .chars()
        .next_back()
        .is_none_or(|value| !is_rust_identifier_continue(value));
    let after = after_text
        .chars()
        .next()
        .is_none_or(|value| !is_rust_identifier_continue(value));
    before && after
}

fn is_rust_identifier(identifier: &str) -> bool {
    let mut chars = identifier.chars();
    let Some(first) = chars.next() else {
        return false;
    };
    (first == '_' || first.is_alphabetic())
        && chars.all(|value| value == '_' || value.is_alphanumeric())
}

fn is_rust_identifier_continue(value: char) -> bool {
    value == '_' || value.is_alphanumeric()
}

fn utf16_character_offset(line_text: &str, byte_start: usize) -> Option<u32> {
    let before_text = line_text.get(..byte_start)?;
    u32::try_from(before_text.encode_utf16().count()).ok()
}

